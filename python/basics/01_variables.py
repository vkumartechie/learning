# Variables in Python
# Variables are containers for storing data values

# String variable
name = "Alice"
print(f"Name: {name}")

# Integer variable
age = 25
print(f"Age: {age}")

# Float variable
height = 5.7
print(f"Height: {height}")

# Boolean variable
is_student = True
print(f"Is Student: {is_student}")

# Multiple assignment
x, y, z = 1, 2, 3
print(f"x={x}, y={y}, z={z}")

# Type checking
print(f"Type of name: {type(name)}")
print(f"Type of age: {type(age)}")
print(f"Type of height: {type(height)}")

# Variable naming rules (valid names)
