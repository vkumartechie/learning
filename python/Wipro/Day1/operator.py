#arithmetic operator

x=10
y=3

#addition
z=x+y
print(f"{x} + {y} = {z}")

#subtraction
z=x-y
print(f"{x} - {y} = {z}")

#multiplication
z=x*y
print(f"{x} * {y} = {z}")

#division
z=x/y
print(f"{x} / {y} = {z}")

#floor division
z=x//y
print(f"{x} // {y} = {z}")

#modulus
z=x%y
print(f"{x} % {y} = {z}")

#exponentiation or power
z=x**y
print(f"{x} ** {y} = {z}")

#comparison operator
x=10
y=20
print(f"{x} == {y} : {x==y}")
print(f"{x} != {y} : {x!=y}")   
 #logical operator
x=True
y=False
print(f"{x} and {y} : {x and y}")
print(f"{x} or {y} : {x or y}")

